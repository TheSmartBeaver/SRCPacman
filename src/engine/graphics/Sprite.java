public class Sprite {

    private int xSprite;
    private int ySprite;

    public Sprite(int xSprite, int ySprite) {
        this.xSprite = xSprite;
        this.ySprite = ySprite;
    }

    public int getxSprite() {
        return xSprite;
    }

    public int getySprite() {
        return ySprite;
    }
}
